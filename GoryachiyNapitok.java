public class GoryachiyNapitok {
    private String name;
    private int massa;

    public GoryachiyNapitok(String name,int massa){
        this.name = name;
        this.massa = massa;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getMassa() {
        return massa;
    }

    public void setMassa(int massa) {
        this.massa = massa;
    }
}
